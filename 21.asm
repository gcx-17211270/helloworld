DATAS SEGMENT
	ORG 50H
    A DQ 79876814H
    B DQ 98734098232983H 
    CA DQ ?
    MESSAGE DW 4 DUP(?),"$"
DATAS ENDS

STACKS SEGMENT
    ;此处输入堆栈段代码
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
    
    LEA SI,A
    LEA DI,B			;数据指针
    MOV BX,10H			;将偏移地址送给BX
    
    MOV CX,4			;循环4次，将原64位数拆分位4次16位数进行加减
AGAIN:
	MOV AX,[SI]
	ADC AX,[DI]
	MOV [BX],AX
	
	ADD SI,2
	ADD DI,2
	ADD BX,2

	LOOP AGAIN

	MOV CX,4			;循环4次，将64位数分成4次显示出来
	LEA SI,CA
	LEA DI,MESSAGE
AGAIN2:
	MOV AX,[SI]
	MOV [DI],AX
	MOV DX,DI
	MOV AH,09H
	INT 21H
	
	ADD SI,2
	ADD DI,2
	LOOP AGAIN2

    MOV AH,4CH
    INT 21H
CODES ENDS
    END START


